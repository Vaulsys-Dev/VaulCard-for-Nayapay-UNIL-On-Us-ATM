package vaulsys.exception;

public class SwitchSystemException extends Exception {
	public SwitchSystemException() {
		super();
	}

	public SwitchSystemException(String message, Throwable cause) {
		super(message, cause);
	}

	public SwitchSystemException(String message) {
		super(message);
	}

	public SwitchSystemException(Throwable cause) {
		super(cause);
	}
}
