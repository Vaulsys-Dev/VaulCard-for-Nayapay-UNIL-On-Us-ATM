package vaulsys.protocols.exception.exception;

public class NotProducedProtocolToBinaryException extends Exception {
    public NotProducedProtocolToBinaryException() {
    }

    public NotProducedProtocolToBinaryException(String message) {
        super(message);
    }

    public NotProducedProtocolToBinaryException(String message, Throwable cause) {
        super(message, cause);
    }

    public NotProducedProtocolToBinaryException(Throwable cause) {
        super(cause);
    }
}
